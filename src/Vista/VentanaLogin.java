    private void jBIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBIngresarActionPerformed
        // TODO add your handling code here:
        if (gestionVentana.verificarDatosIngreso(jTFNickname, jPFContrasenia)) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos para ingresar al Sistema");
        } else {
            String nickname = jTFNickname.getText();
            char[] pass = jPFContrasenia.getPassword();
            String password = new String(pass);// gestionar el ingreso al sistema
            String tipoUsuario = gestionLogin.ingresar(nickname, password);
            switch (tipoUsuario) {
                case "Administrador":
                    JOptionPane.showMessageDialog(null, "Bienvenido Admininstrador " + nickname);
                    new VentanaAdministrador(nickname).setVisible(true);
                    limpiarCamposLogin();
                    break;
                case "Empresario":
                    JOptionPane.showMessageDialog(null, "Bienvenido Empresario " + nickname);
                    new VentanaEmpresario(nickname).setVisible(true);
                    limpiarCamposLogin();
                    break;
                
                case "Proveedor":
                    JOptionPane.showMessageDialog(null, "Bienvenido Proveedor " + nickname);
                    new VentanaProveedor(nickname, valorOferta).setVisible(true);
                    limpiarCamposLogin();
                    break;
                
                default:
                    JOptionPane.showMessageDialog(null, "El usuario no se encuentra registrado");
                    break;
            }
        }

    }//GEN-LAST:event_jBIngresarActionPerformed

