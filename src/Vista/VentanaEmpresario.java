    private void jBEnviarSolicitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEnviarSolicitudActionPerformed
        // TODO add your handling code here:
        if (gestionVentana.verificarDatosEnvioSolicitd(filtro.getList(), jDFechaEntrega, jDFechaFinal,
                jSCantidad, jTADescripcion)) {
            JOptionPane.showMessageDialog(null, "Digita todos los campos para enviar la Subasta");
        } else {
            String nombreProducto = (String) filtro.getList().getSelectedValue();
            int cantidad = Integer.parseInt(jSCantidad.getValue() + "");
            Date fechaEntrega = jDFechaEntrega.getDate();
            Date fechaInicio = gestionVentana.generarFechaActual();
            Date fechaFinal = jDFechaFinal.getDate();
            String descripcion = jTADescripcion.getText();
            Empresarios empre = gestionEmpresario.searchNombre(empresario);
            System.out.println(empresario);
            System.out.println(empre.getCedula());
            Productos pro = gestionProducto.searchNombre(nombreProducto);
            if (fechaInicio.compareTo(fechaFinal) == 0) {
                JOptionPane.showMessageDialog(null, "La Fecha de Cierre deber ser diferente a la Fecha Inicial");
            } else if (fechaInicio.compareTo(fechaFinal) == 1) {
                JOptionPane.showMessageDialog(null, "La Fecha de Cierre deber ser mayor a la Fecha Inicial");
            } else if (fechaFinal.compareTo(fechaEntrega) == 1) {
                JOptionPane.showMessageDialog(null, "La Fecha de Entrega deber ser mayor a la Fecha de Cierre");
            } else {
                if (gestionSubasta.save(cantidad, true, fechaInicio, fechaFinal, fechaEntrega, descripcion, empre.getCedula(),
                        pro.getCodigo())) {
                    JOptionPane.showMessageDialog(null, "La Subasta ha sido creada");
                    limpiarCamposEnvioSolicitud();
                    jTSubasta.setModel(gestionVentana.listarSubastas(gestionEmpresario.searchNombre(empresario).getCedula()));
                }
            }
        }
    }//GEN-LAST:event_jBEnviarSolicitudActionPerformed

    private void jBConsultarOfertasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConsultarOfertasActionPerformed
        // TODO add your handling code here:
        if (gestionVentana.validarSeleccionSolicitud(jTSubasta)) {
            if (!gestionOferta.listOfertaSolicitud(codigoSolicitudOferta).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ofertas Encontradas");
                jTOfertas.setModel(gestionVentana.listarOfertasSubasta(gestionOferta.listOfertaSolicitud(codigoSolicitudOferta)));
            } else {
                JOptionPane.showMessageDialog(null, "No hay Ofertas asociadas a la solicitud seleccionada");
                jTADetallesOferta.setText(null);
                DefaultTableModel vacio = new DefaultTableModel();
                jTOfertas.setModel(gestionVentana.listarOfertasSubasta(gestionOferta.listOfertaSolicitud(codigoSolicitudOferta)));
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una Solicitud para consultar las Ofertas");
        }
    }//GEN-LAST:event_jBConsultarOfertasActionPerformed

